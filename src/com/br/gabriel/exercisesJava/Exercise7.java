package com.br.gabriel.exercisesJava;
/*
 * Escreva um algoritmo que exiba 20 vezes a mensagem
 * “Eu gosto de estudar Algoritmos!”.
 */
public class Exercise7 {
    public static void main(String[] args) {
        for (int i = 1; i <= 20; i++) {
            System.out.println("[" + i + "]" + " Eu gosto de estudar Algoritmos!");
        }
    }
}
